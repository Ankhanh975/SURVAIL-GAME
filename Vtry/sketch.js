function draw() {
  background(100);
  for (var i = 0; i < chunks.length; i++) {
    for (var j = 0; j < chunks[i].length; j++) {
      var x = i * 30;
      var y = j * 30;
      let v = chunks[i][j];
      if (v > 0 && v <= 0.1) {
        fill(128, 128, 0);
      } else if (v > 0.1 && v <= 0.5) {
        fill(0, 255, 0);
      } else if (v > 0.5 && v < 1) {
        fill(128, 128, 128);

        // ttype = "stone";
      } else if (v > -0.2 && v <= 0) {
        fill(0, 0, 240);
      } else {
        fill(10, 10, 200);
      }
      stroke(0);
      rect(x, y, 30, 30);
    }
  }
}
